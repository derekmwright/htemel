func ToggleClassFunc() (*template.Template, ImportSet) {
	tmpl := template.Must(template.New("ToggleClassFunc").
		Funcs(template.FuncMap{
			"titleCase": titleCase,
		}).Parse(`
// ToggleClass toggles a class on or off.
func (e *{{ .Tag | titleCase }}Element) ToggleClass(class string, enable bool) *{{ .Tag | titleCase }}Element {
	classes := strings.Fields(e.attributes["class"].(string))
	idx := slices.Index(classes, class)
	if enable && idx == -1 {
		classes = append(classes, class)
	} else if !enable && idx >= 0 {
		classes = slices.Delete(classes, idx, idx+1)
	}
	e.attributes["class"] = strings.Join(classes, " ")
	return e
}
`))

	return tmpl, nil
}

func RenderFunc() (*template.Template, ImportSet) {
	tmpl := template.Must(template.New("RenderFunc").
		Funcs(template.FuncMap{
			"titleCase": titleCase,
		}).
		Parse(`
// Render processes the current element, and writes the initial tag.
// Then all children are processed and included within the tag.
// Finally, the tag is closed.
//
// *Except for void elements as they are self closing and do not contain children.
func (e *{{ .Tag | titleCase }}Element) Render(w io.Writer) error {
	if e.skipRender {
		return nil
	}

	var sb strings.Builder
	sb.WriteString("<{{ .Tag }}")

	for key, v := range e.attributes {
		sb.WriteByte(' ')
		sb.WriteString(key)

		strVal := fmt.Sprintf("%v", v)
		if strVal != "" {
			sb.WriteByte('=')
			sb.WriteByte('"')
			sb.WriteString(strVal)
			sb.WriteByte('"')
		}
	}

	sb.WriteByte('>')

{{- if not .Void }}
	for _, child := range e.children {
		if err := child.Render(&sb); err != nil {
			return err
		}
	}

	sb.WriteString("</{{ .Tag }}>")
{{- end }}

	_, err := io.WriteString(w, sb.String())
	return err
}
`))

	is := ImportSet{}

	return tmpl, is.Add("io").Add("fmt").Add("golang.org/x/net/html")
}
